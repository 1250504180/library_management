function loadLang(lang) {
    $("[data-i18n]").each(function () {
        const key = $(this).data("i18n");
        if (i18n[lang] && i18n[lang][key]) {
            if (this.tagName.toLowerCase() === "input" && $(this).attr("type") === "button") {
                $(this).val(i18n[lang][key]);
            } else {
                $(this).text(i18n[lang][key]);
            }
        }
    });

    $("[data-i18n-placeholder]").each(function () {
        const key = $(this).data("i18n-placeholder");
        if (i18n[lang] && i18n[lang][key]) {
            $(this).attr("placeholder", i18n[lang][key]);
        }
    });

    $("option[data-i18n]").each(function () {
        const key = $(this).data("i18n");
        if (i18n[lang] && i18n[lang][key]) {
            $(this).text(i18n[lang][key]);
        }
    });

    $("img[data-i18n-alt]").each(function () {
        const key = $(this).data("i18n-alt");
        if (i18n[lang] && i18n[lang][key]) {
            $(this).attr("alt", i18n[lang][key]);
        }
    });

    $("[data-i18n-title]").each(function () {
        const key = $(this).data("i18n-title");
        if (i18n[lang] && i18n[lang][key]) {
            $(this).attr("title", i18n[lang][key]);
        }
    });

    $("[data-i18n-value]").each(function () {
        const key = $(this).attr("data-i18n-value");
        if (i18n[lang][key]) {
            $(this).val(i18n[lang][key]);  // 替换 value
        }
    });
}
